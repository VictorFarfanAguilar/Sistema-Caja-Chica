    public void agregarPersonalComboBox(){
        
        int cantidad = GestionadorCajaChica.persona.size();
        
        String personal[] = new String  [cantidad];
        
        for ( int i=0; i<cantidad; i++){
            
            cbxPersonal.addItem(persona.get(i).getNombre());
        }
        
        
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fecha;
        String comprobante;
        String codigo;
        String concepto;
        String personal;
        float importe;
        String operacion;
        String detalle;
        
        
        fecha = txtfecha.getText();
        comprobante = cbxComprobante.getSelectedItem().toString();
        codigo = txtCodigo.getText();
        concepto = cbxConcepto.getSelectedItem().toString();
        personal = cbxPersonal.getSelectedItem().toString();
        importe = Float.parseFloat(txtImporte.getText());
        operacion = (String)cbxOperacion.getSelectedItem();
       String nom1 = cbxOperacion.getItemAt(0);
       String nom2 = cbxOperacion.getItemAt(1);
       
        
        detalle = txtDetalle.getText();
        System.out.println(operacion);
        
        
        if(!txtfecha.getText().isEmpty()&&!txtCodigo.getText().isEmpty()&&!txtDetalle.getText().isEmpty()){
            if(operacion.equals(nom1)){
            Venta v = new Venta(fecha,comprobante,codigo,concepto,personal,importe,operacion,detalle);
                GestionadorCajaChica.agregarVentaIngreso(v);
       } if(operacion.equals(nom2)){
           Venta v = new Venta(fecha,comprobante,codigo,concepto,personal,importe,operacion,detalle);
                GestionadorCajaChica.agregarVentaEgreso(v);
           
       }    
        }else{
            
            System.out.println("Llenar Campos Vacios ");
        }
       
       

       

    
   

            
        
             
            
       
    }//GEN-LAST:event_jButton1ActionPerformed

